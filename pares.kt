fun main(){
    // Crear un par de una cadena y un número entero
    val par1 = Pair("hola", 42)
    println(par1)

    // Crear un par de dos números flotantes
    val par2 = Pair(3.14f, 2.718f)
    println(par2)

    // Crear un par de un booleano y un carácter
    val par3 = Pair(true, 'x')
    println(par3)
}